package ArrayCreateByxxxx;

import java.util.Arrays;

public class ArrayCreateByNewEx {
	public static void main(String[] args) {
		int[] intArr = new int[10];
		char[] charArr = new char[8];
		double[] doubleArr = new double[11];
		String[] StringArr = new String[12];
		boolean[] booleanArr = new boolean[1];
		
		System.out.println(Arrays.toString(intArr));
		System.out.println(Arrays.toString(charArr));
		System.out.println(Arrays.toString(doubleArr));
		System.out.println(Arrays.toString(StringArr));
		System.out.println(Arrays.toString(booleanArr));
		
	}
}
